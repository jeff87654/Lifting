"""Move the affected combo files currently in parallel_s18/ (which are the
bug-fix-1 regenerated results) to parallel_s18_bugfix1_backup/, so we can
compare fix-1 vs fix-2 results later."""
import os
import shutil

BASE = r"C:\Users\jeffr\Downloads\Lifting\parallel_s18"
BUGFIX1 = r"C:\Users\jeffr\Downloads\Lifting\parallel_s18_bugfix1_backup"
MANIFEST = r"C:\Users\jeffr\Downloads\Lifting\affected_combos.txt"

os.makedirs(BUGFIX1, exist_ok=True)
moved = 0
not_present = 0
with open(MANIFEST) as f:
    for line in f:
        line = line.strip()
        if not line or line.startswith("#"):
            continue
        parts = line.split("\t")
        if len(parts) < 2:
            continue
        partition, fname = parts[0], parts[1]
        src = os.path.join(BASE, partition, fname)
        if not os.path.exists(src):
            not_present += 1
            continue
        dest_dir = os.path.join(BUGFIX1, partition)
        os.makedirs(dest_dir, exist_ok=True)
        shutil.move(src, os.path.join(dest_dir, fname))
        moved += 1

print(f"Moved: {moved}  (these had been regenerated by the fix-1 re-run)")
print(f"Not present in parallel_s18/: {not_present}  (still pending at fix-1 re-run stop)")
